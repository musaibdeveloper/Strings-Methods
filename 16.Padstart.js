// The padStart() method pads the current string with another string at the start. 

let str = "musaib";

console.log(str.padStart(24 , " He is doing good"));
 // He is doing good musaib


// In the above example,
//  we have assigned a string value "musaib" to str and used padStart() to pad "He is doing good" .
//  Inside the method,
//  we have also passed 24 as targetLength.











