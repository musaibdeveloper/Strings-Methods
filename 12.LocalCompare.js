// Local compare method.
// The localeCompare() method checks if a given string comes before, after, or is equivalent as another string in sort order.

// Comparing small character with big gives the output -1.

// Comparing big character with small gives the output 1.


let com = "a".localeCompare("b");

console.log(com); // -1.


let com2 = "d".localeCompare("b");

console.log(com2); // 1. 

// If we compare two equal values, the output is 0.

